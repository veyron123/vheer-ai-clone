#!/bin/bash

echo "Освобождаем порты 5000 и 5178..."

# Находим и убиваем процесс на порту 5000
PORT_5000=$(lsof -ti:5000)
if [ ! -z "$PORT_5000" ]; then
    echo "Закрываем процесс $PORT_5000 на порту 5000"
    kill -9 $PORT_5000 2>/dev/null
else
    echo "Порт 5000 свободен"
fi

# Находим и убиваем процесс на порту 5178
PORT_5178=$(lsof -ti:5178)
if [ ! -z "$PORT_5178" ]; then
    echo "Закрываем процесс $PORT_5178 на порту 5178"
    kill -9 $PORT_5178 2>/dev/null
else
    echo "Порт 5178 свободен"
fi

echo "Порты освобождены!"